public class QRCodeFileEntity implements Serializable{

    private static final long serialVersionUID = -6762647798413291900L;
    private Long id;
    private String url;//	路径
    private String filename;//	名称
    private Long ylId;//	yl---objectid
    private String qrcode;//	二维码编码
    private String createDate;//	创建时间
    private String updateDate;//	修改时间
    private String path;//

    public static long getSerialVersionUID() {
        return serialVersionUID;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUrl() {
        return url==null?"":url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getFilename() {
        return filename==null?"":filename;
    }

    public void setFilename(String filename) {
        this.filename = filename;
    }

    public Long getYlId() {
        return ylId;
    }

    public void setYlId(Long ylId) {
        this.ylId = ylId;
    }

    public String getQrcode() {
        return qrcode==null?"":qrcode;
    }

    public void setQrcode(String qrcode) {
        this.qrcode = qrcode;
    }

    public String getCreateDate() {
        return createDate==null?"":createDate;
    }

    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }

    public String getUpdateDate() {
        return updateDate==null?"":updateDate;
    }

    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }

    public String getPath() {
        return path==null?"":path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}

package com.jymj.zhglxt.zjd.bean.yzt.tg;

import java.math.BigDecimal;

public class TgEntity {
    private BigDecimal area;
    private BigDecimal area1;
    private String code;
    private String geometry;
    private int gid;
    private String landName;
    private String xzqmc;
    
    public BigDecimal getArea() {
        return area==null?BigDecimal.ZERO:area;
    }
    
    public void setArea(BigDecimal area) {
        this.area = area;
    }
    
    public BigDecimal getArea1() {
        return area1==null?BigDecimal.ZERO:area1;
    }
    
    public void setArea1(BigDecimal area1) {
        this.area1 = area1;
    }
    
    public String getCode() {
        return code;
    }
    
    public void setCode(String code) {
        this.code = code;
    }
    
    public String getGeometry() {
        return geometry;
    }
    
    public void setGeometry(String geometry) {
        this.geometry = geometry;
    }
    
    public int getGid() {
        return gid;
    }
    
    public void setGid(int gid) {
        this.gid = gid;
    }
    
    public String getLandName() {
        return landName;
    }
    
    public void setLandName(String landName) {
        this.landName = landName;
    }
    
    public String getXzqmc() {
        return xzqmc;
    }
    
    public void setXzqmc(String xzqmc) {
        this.xzqmc = xzqmc;
    }
}

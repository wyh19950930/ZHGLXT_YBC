package com.jymj.zhglxt.zjd.bean.yzt.illegal;

import java.io.Serializable;

/**
 * Created by dl on 2019/10/11.
 */
public class IllegalFileEntity implements Serializable {

    private Integer id;
    private String path;
    private Integer illegalId;
    private String filename;

    public String getFilename() {
        return filename;
    }

    public void setFilename(String filename) {
        this.filename = filename;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPath() {
        return path==null?"":path.replace("\\","/");
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Integer getIllegalId() {
        return illegalId;
    }

    public void setIllegalId(Integer illegalId) {
        this.illegalId = illegalId;
    }
}

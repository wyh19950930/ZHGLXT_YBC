package com.jymj.zhglxt.zjd.bean.jsjb;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by ${lc} on 2022/3/10.处理文件
 */
public class OpinionFile implements Serializable {
    private static final long serialVersionUID = -3766363982448204447L;
    private Long id;
    private String filename;//	文件名
    private Integer filetype;//	文件类型
    private String filelabel;//	文件标签类型
    private String uploadunit;//	上传单位
    private String createDate;//	创建时间
    private String updateDate;//	修改时间
    private String fileSize;//	文件大小
    private String url;//	路径
    private String path;//	路径
    private Long opinionId;//	public_opinion----id

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFilename() {
        return filename==null?"":filename;
    }

    public void setFilename(String filename) {
        this.filename = filename;
    }

    public Integer getFiletype() {
        return filetype;
    }

    public void setFiletype(Integer filetype) {
        this.filetype = filetype;
    }

    public String getFilelabel() {
        return filelabel==null?"":filelabel;
    }

    public void setFilelabel(String filelabel) {
        this.filelabel = filelabel;
    }

    public String getUploadunit() {
        return uploadunit==null?"":uploadunit;
    }

    public void setUploadunit(String uploadunit) {
        this.uploadunit = uploadunit;
    }

    public String getCreateDate() {
        return createDate==null?"":createDate;
    }

    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }

    public String getUpdateDate() {
        return updateDate==null?"":updateDate;
    }

    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }

    public String getUrl() {
        return url==null?"":url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getFileSize() {
        return fileSize==null?"":fileSize;
    }

    public void setFileSize(String fileSize) {
        this.fileSize = fileSize;
    }

    public String getPath() {
        return path==null?"":path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Long getOpinionId() {
        return opinionId;
    }

    public void setOpinionId(Long opinionId) {
        this.opinionId = opinionId;
    }
}

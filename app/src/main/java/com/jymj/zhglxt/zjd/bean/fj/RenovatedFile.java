package com.jymj.zhglxt.zjd.bean.fj;

import java.io.Serializable;

/**
 * @author Lzh
 * @Date 2021/5/26 14:52
 */
public class RenovatedFile implements Serializable {
    private static final long serialVersionUID = 6535860514598916329L;
    private Long id;
    private Long msid;//renovated主键
    private String name;
    private String path;
    private String entime;//上传时间
    private Long enpsn;//上传人
    private Integer filetype;//项目状态文件
    private Long ylId;//yl_objectid
    private String jlrname;//记录人姓名
    private String jlrtel;//记录人电话
    private Long type;//类型 1 图片  2 视频 3 文件
    private String url;//url地址

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getMsid() {
        return msid;
    }

    public void setMsid(Long msid) {
        this.msid = msid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getEntime() {
        return entime;
    }

    public void setEntime(String entime) {
        this.entime = entime;
    }

    public Long getEnpsn() {
        return enpsn;
    }

    public void setEnpsn(Long enpsn) {
        this.enpsn = enpsn;
    }

    public Integer getFiletype() {
        return filetype;
    }

    public void setFiletype(Integer filetype) {
        this.filetype = filetype;
    }

    public Long getYlId() {
        return ylId;
    }

    public void setYlId(Long ylId) {
        this.ylId = ylId;
    }

    public String getJlrname() {
        return jlrname;
    }

    public void setJlrname(String jlrname) {
        this.jlrname = jlrname;
    }

    public String getJlrtel() {
        return jlrtel;
    }

    public void setJlrtel(String jlrtel) {
        this.jlrtel = jlrtel;
    }

    public Long getType() {
        return type;
    }

    public void setType(Long type) {
        this.type = type;
    }

    public String getUrl() {
        return url==null?"":url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}

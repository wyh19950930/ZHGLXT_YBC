package com.jymj.zhglxt.zjd.bean.yzt.illegal;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

/**
 * Created by dl on 2019/5/16.
 */
public class GhFhEntity implements Serializable {

    private String proj;//项目
    private List<GhEntity> entityList;
    private BigDecimal zmj;//总面积
    private BigDecimal zjjg;//总价格
    private BigDecimal jzjg;//均价格
    private boolean isOpen;

    public boolean isOpen() {
        return isOpen;
    }

    public void setOpen(boolean open) {
        isOpen = open;
    }

    public String getProj() {
        return proj==null?"":proj;
    }

    public void setProj(String proj) {
        this.proj = proj;
    }

    public List<GhEntity> getEntityList() {
        return entityList;
    }

    public void setEntityList(List<GhEntity> entityList) {
        this.entityList = entityList;
    }

    public BigDecimal getZmj() {
        return zmj==null?new BigDecimal(0):zmj;
    }

    public void setZmj(BigDecimal zmj) {
        this.zmj = zmj;
    }

    public BigDecimal getZjjg() {
        return zjjg==null?new BigDecimal(0):zjjg;
    }

    public void setZjjg(BigDecimal zjjg) {
        this.zjjg = zjjg;
    }

    public BigDecimal getJzjg() {
        return jzjg;
    }

    public void setJzjg(BigDecimal jzjg) {
        this.jzjg = jzjg;
    }

}

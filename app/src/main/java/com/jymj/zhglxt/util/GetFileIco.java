package com.jymj.zhglxt.util;

import com.jymj.zhglxt.R;
import com.setsuna.common.commonutils.StringUtils;

/**
 * Created by Administrator on 2016/9/21.
 */
//获取文件的后缀
public class GetFileIco {
    public static int getIcoId(String ico){
        if (StringUtils.isEmpty(ico)){
            return R.drawable.ic_type_unknow;
        }
        if ("mp3".equalsIgnoreCase(ico)){
            return R.drawable.ft_mp3;
        }
        if ("mp2".equalsIgnoreCase(ico)){
            return R.drawable.ft_mp2;
        }
        if ("mp2v".equalsIgnoreCase(ico)){
            return R.drawable.ft_mp2v;
        }
        if ("mp4".equalsIgnoreCase(ico)){
            return R.drawable.ft_mp4;
        }
        if ("mpeg".equalsIgnoreCase(ico)){
            return R.drawable.ft_mpeg;
        }
        if ("mpg".equalsIgnoreCase(ico)){
            return R.drawable.ft_mpg;
        }
        if ("bmp".equalsIgnoreCase(ico)){
            return R.drawable.ft_bmp;
        }
        if ("epub".equalsIgnoreCase(ico)){
            return R.drawable.ft_epub;
        }
        if ("avi".equalsIgnoreCase(ico)){
            return R.drawable.ft_avi;
        }
        if ("wav".equalsIgnoreCase(ico)){
            return R.drawable.ft_wav;
        }
        if ("gif".equalsIgnoreCase(ico)){
            return R.drawable.ft_gif;
        }
        if ("ico".equalsIgnoreCase(ico)){
            return R.drawable.ft_ico;
        }
        if ("zip".equalsIgnoreCase(ico)){
            return R.drawable.ft_zip;
        }
        if ("pdf".equalsIgnoreCase(ico)){
            return R.drawable.ft_pdf;
        }
        if ("ppt".equalsIgnoreCase(ico)){
            return R.drawable.ft_ppt;
        }
        if ("html".equalsIgnoreCase(ico)){
            return R.drawable.ft_html;
        }
        if ("pptx".equalsIgnoreCase(ico)){
            return R.drawable.ft_pptx;
        }
        if ("doc".equalsIgnoreCase(ico)){
            return R.drawable.ft_doc;
        }
        if ("docx".equalsIgnoreCase(ico)){
            return R.drawable.ft_docx;
        }
        if ("xls".equalsIgnoreCase(ico)){
            return R.drawable.ft_xls;
        }
        if ("xlsx".equalsIgnoreCase(ico)){
            return R.drawable.ft_xlsx;
        }
        if ("jpg".equalsIgnoreCase(ico)){
            return R.drawable.ft_jpg;
        }
        if ("png".equalsIgnoreCase(ico)){
            return R.drawable.ft_png;
        }

        return R.drawable.ic_type_unknow;
    }
}
